// Write a program to calculate the average marks of a student.
// Create the calculateAverage() function
function calculateAverage(mark1, mark2, mark3) {
  return (mark1 + mark2 + mark3) / 3;
}

// Call the function with arguments mark1, mark2, and mark3
let averageMarks = calculateAverage(61, 66, 88);

console.log(averageMarks);
